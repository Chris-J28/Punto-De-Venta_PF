    private void btnQuitarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnQuitarActionPerformed
        int filaSeleccionada = tablaCarrito.getSelectedRow();

        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(
                    this,
                    "Selecciona un producto del carrito para quitarlo.",
                    "Ninguna fila seleccionada",
                    JOptionPane.WARNING_MESSAGE
            );
            return;
        }

        // Obtenemos el ID del producto desde la tabla
        int idProducto = (int) tablaCarrito.getValueAt(filaSeleccionada, 0);
        Producto producto = com.mycompany.proyecto_final.Sistema.inventario.buscarPorId(idProducto);

        // Aquí asumimos que Carrito tiene un método quitarProducto(Producto p)
        carrito.quitarProducto(producto);

        // Volvemos a cargar la tabla y el total
        cargarTablaCarrito();
        actualizarTotal();
    }//GEN-LAST:event_btnQuitarActionPerformed
/**
     * Evento que se ejecuta cuando el cliente hace clic en
     * "Vaciar carrito". Elimina todos los productos del carrito.
     */
    private void btnVaciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVaciarActionPerformed
         int opcion = JOptionPane.showConfirmDialog(
                this,
                "¿Seguro que deseas vaciar todo el carrito?",
                "Confirmar",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.WARNING_MESSAGE
        );

        if (opcion == JOptionPane.YES_OPTION) {
            // Asumimos que Carrito tiene un método vaciar()
            carrito.vaciar();

            cargarTablaCarrito();
            actualizarTotal();
        }
    }//GEN-LAST:event_btnVaciarActionPerformed
