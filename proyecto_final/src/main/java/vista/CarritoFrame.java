/**
     * Llena la tabla "tablaCarrito" con los productos que
     * el cliente tiene actualmente en su carrito.
     *
     * NOTA: Ajusta este método de acuerdo a cómo esté implementado
     * tu clase Carrito (por ejemplo, lista de líneas, mapa, etc.).
     */
    private void cargarTablaCarrito() {
        DefaultTableModel modelo = (DefaultTableModel) tablaCarrito.getModel();
        modelo.setRowCount(0); // Limpiar filas

        // Ejemplo asumiendo que Carrito tiene un método getLineas()
        // que devuelve una lista de objetos LineaCarrito con:
        // - getProducto()
        // - getCantidad()
        for (Carrito.LineaCarrito linea : carrito.getLineas()) {
            Producto p = linea.getProducto();
            int cantidad = linea.getCantidad();
            double subtotal = p.getPrecio() * cantidad;

            Object[] fila = new Object[5];
            fila[0] = p.getId();
            fila[1] = p.getNombre();
            fila[2] = p.getPrecio();
            fila[3] = cantidad;
            fila[4] = subtotal;

            modelo.addRow(fila);
        }

        tablaCarrito.setModel(modelo);
    }
    /**
     * Calcula el total del carrito sumando los subtotales
     * de cada línea y lo muestra en la etiqueta lblTotalValor.
     */
    private void actualizarTotal() {
        double total = 0.0;

        DefaultTableModel modelo = (DefaultTableModel) tablaCarrito.getModel();
        int filas = modelo.getRowCount();

        for (int i = 0; i < filas; i++) {
            double subtotal = (double) modelo.getValueAt(i, 4); // Columna "Subtotal"
            total += subtotal;
        }

        lblTotalValor.setText(String.format("$ %.2f", total));
    }
