/**
 * Evento que se ejecuta cuando el cliente hace clic en "Agregar al carrito".
 * Toma el producto seleccionado en la tabla y pregunta cuántas unidades
 * desea agregar al carrito, validando que haya stock suficiente.
 */
    private void btnAgregarAlCarritoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarAlCarritoActionPerformed
      // 1. Verificamos que haya una fila seleccionada en la tabla
    int filaSeleccionada = tablaProductosCliente.getSelectedRow();

    if (filaSeleccionada == -1) {
        JOptionPane.showMessageDialog(
                this,
                "Por favor selecciona un producto de la tabla.",
                "Ningún producto seleccionado",
                JOptionPane.WARNING_MESSAGE
        );
        return;
    }

    // 2. Obtenemos el ID del producto desde la tabla (columna 0)
    int idProducto = (int) tablaProductosCliente.getValueAt(filaSeleccionada, 0);

    // 3. Buscamos el producto real en el inventario usando el ID
    Producto productoSeleccionado = Sistema.inventario.buscarPorId(idProducto);

    if (productoSeleccionado == null) {
        JOptionPane.showMessageDialog(
                this,
                "No se encontró el producto en el inventario.",
                "Error",
                JOptionPane.ERROR_MESSAGE
        );
        return;
    }

    // 4. Pedimos al cliente la cantidad que desea agregar
    String textoCantidad = JOptionPane.showInputDialog(
            this,
            "¿Cuántas unidades de \"" + productoSeleccionado.getNombre() + "\" deseas agregar al carrito?",
            "Cantidad",
            JOptionPane.QUESTION_MESSAGE
    );

    // Si el cliente cancela el cuadro de diálogo
    if (textoCantidad == null) {
        return;
    }

    try {
        int cantidad = Integer.parseInt(textoCantidad);

        if (cantidad <= 0) {
            JOptionPane.showMessageDialog(
                    this,
                    "La cantidad debe ser mayor que cero.",
                    "Cantidad inválida",
                    JOptionPane.WARNING_MESSAGE
            );
            return;
        }

        // 5. Validamos que haya stock suficiente en el inventario
        if (cantidad > productoSeleccionado.getStock()) {
            JOptionPane.showMessageDialog(
                    this,
                    "No hay suficiente stock.\nStock disponible: " + productoSeleccionado.getStock(),
                    "Stock insuficiente",
                    JOptionPane.WARNING_MESSAGE
            );
            return;
        }

        // 6. Agregamos el producto al carrito (sin modificar aún el stock real del inventario)
        carrito.agregarProducto(productoSeleccionado, cantidad);

        JOptionPane.showMessageDialog(
                this,
                "Se agregaron " + cantidad + " unidades de \"" 
                + productoSeleccionado.getNombre() + "\" al carrito.",
                "Producto agregado",
                JOptionPane.INFORMATION_MESSAGE
        );

    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(
                this,
                "La cantidad debe ser un número entero.",
                "Error de formato",
                JOptionPane.ERROR_MESSAGE
        );
    }
    }//GEN-LAST:event_btnAgregarAlCarritoActionPerformed

