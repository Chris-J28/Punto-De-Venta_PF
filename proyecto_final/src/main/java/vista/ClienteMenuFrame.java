    /**
 * Llena la tabla "tablaProductosCliente" con los productos
 * que existen actualmente en el inventario del sistema.
 */
private void cargarTablaProductosCliente() {
    // Obtenemos el modelo de la tabla
    DefaultTableModel modelo = (DefaultTableModel) tablaProductosCliente.getModel();

    // Limpiamos filas anteriores
    modelo.setRowCount(0);

    // Recorremos los productos del inventario de Chapi's
    for (Producto p : Sistema.inventario.getProductos()) {
        Object[] fila = new Object[4];
        fila[0] = p.getId();
        fila[1] = p.getNombre();
        fila[2] = p.getPrecio();
        fila[3] = p.getStock();

        modelo.addRow(fila);
    }

    tablaProductosCliente.setModel(modelo);
}

